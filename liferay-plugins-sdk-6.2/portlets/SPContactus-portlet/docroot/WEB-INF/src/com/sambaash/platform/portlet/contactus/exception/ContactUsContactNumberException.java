package com.sambaash.platform.portlet.contactus.exception;

import javax.portlet.PortletException;

public class ContactUsContactNumberException extends PortletException {

	public ContactUsContactNumberException() {
		super();
	}

	public ContactUsContactNumberException(String msg) {
		super(msg);
	}

	public ContactUsContactNumberException(String msg, Throwable cause) {
		super(msg, cause);
	}

	public ContactUsContactNumberException(Throwable cause) {
		super(cause);
	}

}
