package com.sambaash.platform.portlet.spcontent.resource;

@SuppressWarnings("serial")
public class ServeResourceException extends Exception {

	public ServeResourceException() {
		super();
	}

	public ServeResourceException(String arg0) {
		super(arg0);
	}

	public ServeResourceException(Throwable arg0) {
		super(arg0);
	}

	public ServeResourceException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ServeResourceException(String arg0, Throwable arg1, boolean arg2,
			boolean arg3) {
		super(arg0, arg1, arg2, arg3);
	}

}
